fruta = input("Digite a fruta: ").upper()
if fruta == 'BANANA':
    print(" O preço do quilo da banana é R$ 5,23 ")
elif fruta == 'MAÇA':
    print(" O preço do quilo da maça é R$ 12,10 ")
elif fruta == 'CEREJA':
    print(" O preço do quilo da cereja é R$ 58,00")
else:
    print("Desculpe, não temos", fruta)